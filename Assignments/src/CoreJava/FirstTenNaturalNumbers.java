package CoreJava;

public class FirstTenNaturalNumbers {

    public static void main(String[] args) {
        int i = 1;

        // Display the first 10 natural numbers
        System.out.println("The first 10 natural numbers are: ");
        while (i <= 10) {
            System.out.print(i + " ");
            i++;
        }
    }
}
